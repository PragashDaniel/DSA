package com.zsg.practice;

public class ExNo05 {

	public static void main(String[] args) 
	{
		//& | ^ ~ << >> >>>
		int a=4,b=2;
		System.out.println("a & b : "+(a&b));
		System.out.println("a | b : "+(a|b));
		System.out.println("a ^ b : "+(a^b));
		System.out.println("a << b : "+(a<<b));
		System.out.println("a >> b : "+(a>>b));
		System.out.println("a >>> b : "+(a>>>b));
	}

}
